import numpy as np


def euclidian_distance(vec_a, vec_b):
    """
    计算向量a与向量b的欧氏距离
    :param vec_a:
    :param vec_b:
    :return: 欧氏距离
    """
    return np.sqrt(np.sum(np.square(np.array(vec_a) - vec_b)))
